void
solve(void) {
    int n;
    std::cin >> n;
    std::vector<i64> vec(n);
    for (auto &&v : vec) {
        std::cin >> v;
    }
    i64 sum  = 0;
    i64 maxi = 0;
    i64 res  = 0;
    for (int i = 0; i < n; i++) {
        sum += vec[i];
        maxi = std::max(maxi, vec[i]);
        if (sum - maxi == maxi) {
            res++;
        }
    }
    std::cout << res << "\n";
}

