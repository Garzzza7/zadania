void
solve(void) {
    i32 n;
    std::cin >> n;
    str s;
    std::cin >> s;
    i32 res{0};
    std::vector<bool>seen(1000,false);
    for(const auto& v : s) {
        if(seen[v-'A']) {
            res++;
        } else {
            res+=2;
            seen[v-'A'] = true;
        }
    }
    std::cout << res << "\n";
}

