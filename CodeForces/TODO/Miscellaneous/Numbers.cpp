void solve(void) {
    int a;
    std::cin >> a;
    int top = 0;
    int bot = 0;
    for(int i = 2 ; i <= a - 1 ; i++) {
        auto tmp = a;
        while(tmp) {
            top += tmp % i;
            tmp /= i;
        }
        bot++;
    }
    auto g = std::gcd(top , bot);
    std::cout << top / g << "/" << bot / g << "\n"; 
}

int main(void) {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    std::cout.tie(nullptr);

    int _{1};
    while (_--) {
        solve();
    }

    return 0;
}
