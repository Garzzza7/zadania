void solve(void) {
    int n;
    std::cin >> n;
    int res = 0;
    for (int a = 1; a < n; a++) {
        for (int b = a + 1; b < n; b++) {
            int c = (int) std::sqrt(a * a + b * b);
            if ((a * a + b * b == (c * c)) and c > b and c <= n) { res++; }
        }
    }
    std::cout << res << "\n";
}

