void
solve(void) {
    i64 n , a , b;
    std::cin >> n >> a >> b;
    i64 res = 0LL;
    i64 limit = std::max(0LL , a - b);
    i64 l = limit/2LL * (b + b + limit);
    i64 r = (n-limit)*a;
    res = l + r;
    std::cout << res << "\n";
}

