template <typename T>
[[nodiscard]] inline T
bin_ce(T x, T y) {
    return x / y + ((x ^ y) > 0 && x % y);
}

void
solve() {
    u64 n, k;
    std::cin >> n >> k;
    u64 h = k / (1ULL / 2ULL * n + 1ULL);
    std::cout << bin_ce(h, n) << "\n";
}

