void
solve(void) {
    int n, m, k;
    std::cin >> n >> m >> k;
    std::vector<int> s(n);
    for (auto &&v : s)
        std::cin >> v;
    for (int _ = 0; _ < m; _++) {
        int p, v;
        std::cin >> p >> v;
        p--;
        s[p] = v;
    }
    u64 res = 0;
    for (const auto &v : s) {
        res += v < k;
    }
    std::cout << res << "\n";
}

int
main(void) {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    std::cout.tie(nullptr);

    int _{1};
    while (_--) {
        solve();
    }

    return 0;
}
