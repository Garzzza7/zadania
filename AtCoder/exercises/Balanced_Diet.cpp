void
solve(void) {
    int n;
    str s;
    std::cin >> n;
    std::cin >> s;
    std::map<i64, i64> map;
    i64 res  = 0;
    i64 curr = 0;
    map[curr]++;
    for (const auto &c : s) {
        curr += c == 'V';
        curr -= c == 'F';
        res += map[curr];
        map[curr]++;
    }
    std::cout << res << "\n";
}

